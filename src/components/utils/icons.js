      const g = (d, c) => h("g", d, c);
      const path = (d, c) => h("path", d, c);

  const setPath = (d) =>
        path(
          {
              fill: gradient? `${url}`: "currentColor",
              d,
          }
          // [title(props?.title || `${getName} icon`)]
    );
  
